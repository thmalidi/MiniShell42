#include "libft.h"

int	nbrlen16(long int nb)
{
	long int	i;

	i = 0;
	if (nb == 0)
		return (1);
	if (nb < 0)
		i += 1;
	while (nb)
	{
		nb = nb / 16;
		i++;
	}
	return (i);
}
