#include "libft.h"

int	treat_u(va_list pmt)
{
	long int	d;

	d = va_arg(pmt, int);
	if (d < 0)
		d = 4294967296 - (d * -1);
	ft_putnbr_l(d, 1);
	return (nbrlen_l(d));
}
