#include "libft.h"

void	ft_putnbr_l(long int n, int fd)
{
	char	temp[12];
	int		i;

	i = 0;
	if (n == 0)
		write(1, "0", 1);
	while (n != 0)
	{
		temp[i++] = (char)((n % 10) + 48);
		n = n / 10;
	}
	i -= 1;
	while (i >= 0)
		write(fd, &temp[i--], 1);
}
