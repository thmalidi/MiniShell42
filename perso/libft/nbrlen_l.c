#include "libft.h"

int	nbrlen_l(long int nb)
{
	int	i;

	i = 0;
	if (nb == 0)
		return (1);
	if (nb < 0)
	{
		i += 1;
		nb = nb * -1;
	}
	while (nb)
	{
		nb = nb / 10;
		i++;
	}
	return (i);
}
